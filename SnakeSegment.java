
public class SnakeSegment {
	private int xCor;
	private int yCor;
	
	public SnakeSegment(int x, int y) {
		this.xCor = x;
		this.yCor = y;
	}
	
	public int getXCor() {
		return xCor;
	}
	
	public int getYCor() {
		return yCor;
	}
	
	public void setCor(int x, int y) {
		this.xCor = x;
		this.yCor = y;
	}

}
